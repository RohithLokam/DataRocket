package com.DataRockect.ProjectCreation;

public class ProjectCreation {
	
	private String userName;
	private String password;
	private String projectName;
	private String description;
	private String startDate;
	private String endDate;
	
	private String requiredEmployes;
	private String projectManager;
	private String hr;
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getRequiredEmployes() {
		return requiredEmployes;
	}
	public void setRequiredEmployes(String requiredEmployes) {
		this.requiredEmployes = requiredEmployes;
	}
	public String getProjectManager() {
		return projectManager;
	}
	public void setProjectManager(String projectManager) {
		this.projectManager = projectManager;
	}
	public String getHr() {
		return hr;
	}
	public void setHr(String hr) {
		this.hr = hr;
	}

	
}
